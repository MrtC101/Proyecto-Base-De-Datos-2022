    private void SPSearch_bttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SPSearch_bttnActionPerformed
        // TODO add your handling code here:
        JTable table =PanelManager.view.getTable();
        try{
            if(this.PSCountry_Rad.isSelected()){
                String[] fields= {"Countrys"};
                imp.SearchCountrys(table,fields);
            }else if(this.PSState_Rad.isSelected()){
                String[] fields= {"States"};
                imp.SearchStates(table,fields);
            }else if(this.PSZone_Rad.isSelected()){
                String[] fields= {"Zone"};
                imp.SearchZones(table,fields);
            }else if(this.PSGlobal_Rad.isSelected()){
                String[] fields= {"Country","State","Zone"};
                imp.SearchGlobalLocations(table,fields);
            }else if(this.PSMail_Rad.isSelected()){
                String[] fields= {"Mail"};
                imp.SearchMails(table,fields);
            }else if(this.PSPhone_Rad.isSelected()){
                String[] fields= {"Phone"};
                imp.SearchPhones(table,fields);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(jDialog1, e.getMessage());
        }
    }//GEN-LAST:event_SPSearch_bttnActionPerformed

