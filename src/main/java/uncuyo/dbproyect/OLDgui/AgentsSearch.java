    private void SASearch_bttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SASearch_bttnActionPerformed
        // TODO add your handling code here:
        ArrayList fields = new ArrayList();
        if(SAid_Box.isSelected()){
            fields.add("ID");
        }
        if(SARole_Box.isSelected()){
            fields.add("Role");
        }
        if(SAName_Box.isSelected()){
            fields.add("Name");
        }
        if(SALastName_Box.isSelected()){
            fields.add("Last Name");
        }
        if(SANationalId_Box.isSelected()){
            fields.add("Natinal ID");
        }
        if(SABornDate_Box.isSelected()){
            fields.add("Born Date");
        }
        if(SAPhone_Box.isSelected()){
            fields.add("Phone");
        }
        if(SAMail_Box.isSelected()){
            fields.add("Mail");
        }
        if(SAEmergencyPhone_Box.isSelected()){
            fields.add("Emergency Phone");
        }
        if(SAHealth_Box.isSelected()){
            fields.add("Health Ensurance");
        }
        if(SACountry_Box.isSelected()){
            fields.add("Country");
        }
        if(SAState_Box.isSelected()){
            fields.add("State");
        }
        if(SAZone_Box.isSelected()){
            fields.add("Zone");
        }
        if(SAStreet_Box.isSelected()){
            fields.add("Street");
        }
        try{
            switch((String) SALook_Box.getSelectedItem()){
                case "all Agents":
                    imp.SearchAgents( SATable,fields);
                    break;
                case "ID":
                    imp.SearchID(Integer.parseInt(SAValue_txf.getText()), SATable,fields);
                    break;
                case "Role":
                    imp.SearchRole(SAValue_txf.getText(), SATable,fields);
                    break;
                case "Name":
                    imp.SearchName(SAValue_txf.getText(), SATable,fields);
                    break;
                /*case "Last Name":
                    imp.SearchLastName(SAValue_txf.getText(), SATable,fields);
                    break;*/
                case "National ID Number":
                    imp.SearchNational(SAValue_txf.getText(), SATable,fields);
                    break;
                /*case "Health Ensurance":
                    imp.SearchEnsurance(SAValue_txf.getText(), SATable,fields);
                    break;*/
                case "Phone Number":
                    imp.SearchPhone(SAValue_txf.getText(), SATable,fields);
                    break;
                /*case "Emergency Phone Number":
                    imp.SearchEmergency(SAValue_txf.getText(), SATable,fields);
                    break;*/
                case "Street":
                    imp.SearchAddress(SAValue_txf.getText(), SATable,fields);
                    break;  
                case "Mail":
                    imp.SearchMail(SAValue_txf.getText(), SATable,fields);
                    break;                  
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(jDialog1, e.getMessage());
        }
    }//GEN-LAST:event_SASearch_bttnActionPerformed

    private void SPSearch_bttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SPSearch_bttnActionPerformed
        // TODO add your handling code here:
        try{
        if(this.PSCountry_Rad.isSelected()){
            String[] fields= {"Countrys"};
            imp.SearchCountrys( PSTable,fields);
        }else if(this.PSState_Rad.isSelected()){
            String[] fields= {"States"};
            imp.SearchStates(PSTable,fields);
        }else if(this.PSZone_Rad.isSelected()){
            String[] fields= {"Zone"};
            imp.SearchZones( PSTable,fields);
        }else if(this.PSGlobal_Rad.isSelected()){
            String[] fields= {"Country","State","Zone"};
            imp.SearchGlobalLocations( PSTable,fields);
        }else if(this.PSMail_Rad.isSelected()){
            String[] fields= {"Mail"};
            imp.SearchMails( PSTable,fields);
        }else if(this.PSPhone_Rad.isSelected()){
            String[] fields= {"Phone"};
            imp.SearchPhones( PSTable,fields);
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(jDialog1, e.getMessage());
        }
    }//GEN-LAST:event_SPSearch_bttnActionPerformed


