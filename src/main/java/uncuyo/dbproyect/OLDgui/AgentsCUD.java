    private void Update_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Update_btnActionPerformed
        // TODO add your handling code here:
        int id =Integer.parseInt(Uid_txf.getText());
        try{
            if(this.UName_Box.isSelected()){
                imp.UpdateName(id,UName_txf.getText());
            }
            if(this.ULastName_Box.isSelected()){
                imp.UpdateLastName(id,ULastName_txf.getText());
            }
            if(this.UBornDate_Box.isSelected()){
                imp.UpdateBornDate(id,new Date(UBornDate_formatted.getText()));
            }
            if(this.UNationalID_Box.isSelected()){
                imp.UpdateNationalNumber(id,this.UNationalNumber_formatted.getText());
            }
            if(URole_Box2.isSelected()){
                if(URole_Box.isSelected()){
                    imp.UpdateRole(id,this.URole_txf.getText());
                }else{
                    imp.UpdateRole(id,(String) URole_List.getSelectedItem());
                }
            }
            if(this.UAddress_Box.isSelected()){
                String country;
                String state;
                String zone;
                if(this.UCountry_Box.isSelected()){
                    country = this.UCountry_txf.getText();
                }else{
                    country = (String) this.UCountry_List.getSelectedItem();
                }
                if(this.UState_Box.isSelected()){
                   state = this.UState_txf.getText();
                }else{
                    state = (String) this.UState_List.getSelectedItem();
                }
                if(this.UZone_Box.isSelected()){
                    zone = this.UZone_txf.getText();
                }else{
                    zone = (String) this.UZone_List.getSelectedItem();
                }
                imp.UpdateAddress(id,country,state,zone,this.UStreet_txf.getText(),this.UNumber_txf.getText());
            }
            if(this.UHealth_Box.isSelected()){
                imp.UpdateHealth(id,UHealthEnsurance_txf.getText());
            }
            if(this.UEmergencyPhone_Box.isSelected()){
                imp.UpdateEmergencyPhone(id,new PhoneNumber(UEmergencyPhone_formatted.getText()));
            }
            if(this.UPhone_Box.isSelected()){
                imp.UpdatePhone(id,new PhoneNumber(UphoneNumber_formatted.getText()));
            }
            if(this.UMail_Box.isSelected()){
                imp.UpdateMail(id,new Mail(Umail_formatted.getText()));
            }
            UStatus_txf.setText("Succed.Agent Updated");
        }catch(Exception e){
            UStatus_txf.setText("Error.Agent Not Updated");
            JOptionPane.showMessageDialog(jDialog1, e.getMessage());
        }
    }//GEN-LAST:event_Update_btnActionPerformed

